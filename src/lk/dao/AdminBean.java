package lk.dao;

public class AdminBean {

	private int admin_ID;
	private String admin_name;
	private String admin_password;
	private int admin_right;
	
	public int getAdmin_ID() {
		return admin_ID;
	}
	public void setAdmin_ID(int adminID) {
		admin_ID = adminID;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String adminName) {
		admin_name = adminName;
	}
	public String getAdmin_password() {
		return admin_password;
	}
	public void setAdmin_password(String adminPassword) {
		admin_password = adminPassword;
	}
	public int getAdmin_right() {
		return admin_right;
	}
	public void setAdmin_right(int adminRight) {
		admin_right = adminRight;
	}
	
	
	
	
	
}
